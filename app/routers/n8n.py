# --- LinkedIn scraping (stays in Python, browser needed) ---

@router.post("/scrape")
async def scrape(request: Request, queries: list[dict] = Body(...)):
    """Execute LinkedIn scraping. Browser stays in Python."""
    from app.repositories.prospect_repo import ProspectRepository
    from app.scraper.rate_limiter import DailyLimitReached
    from app.scheduler.jobs import _auto_relogin

    scraper = request.app.state.scraper
    if not scraper:
        return JSONResponse({"error": "Scraper not initialized"}, 500)

    if not scraper._browser:
        _step(request, "searching", "n8n: demarrage navigateur...")
        await scraper.start()

    session_valid = await scraper.is_session_valid()
    if not session_valid:
        _step(request, "searching", "n8n: session expiree, tentative reconnexion...")
        session_valid = await _auto_relogin(scraper)
        if not session_valid:
            return JSONResponse({"error": "LinkedIn session expired", "session_valid": False}, 200)

    total_found = 0
    total_new = 0
    rate_limited = False

    async with get_db() as db:
        repo = ProspectRepository(db)

        for i, q in enumerate(queries[:10]):
            kw = q["keywords"]
            loc = q.get("location")
            search_id = q.get("id")
            _step(request, "searching", f"n8n: recherche {i+1}/{len(queries[:10])}: {kw[:40]}...")

            try:
                results = await scraper.search_people(kw, loc)

                if results:
                    for pg in range(2, 51):
                        await asyncio.sleep(random.uniform(2.0, 5.0))
                        try:
                            pg_results = await scraper.search_people(kw, loc, page=pg)
                            if not pg_results:
                                break
                            results.extend(pg_results)
                        except DailyLimitReached:
                            raise
                        except Exception:
                            break

                if not results:
                    simplified = " ".join(kw.split()[:2])
                    if simplified != kw:
                        await asyncio.sleep(random.uniform(2.0, 4.0))
                        results = await scraper.search_people(simplified, loc)

                total_found += len(results)

                for p in results:
                    username = (p.get("profile_username") or "").strip("/").split("/")[-1]
                    if not username or len(username) < 2:
                        continue
                    _, is_new = await repo.upsert_by_username(username, {
                        "full_name": p.get("full_name", ""),
                        "headline": p.get("headline", ""),
                        "location": p.get("location", ""),
                        "linkedin_url": f"https://www.linkedin.com/in/{username}/",
                        "linkedin_username": username,
                        "source_search_id": search_id,
                        "scraped_at": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
                    })
                    if is_new:
                        total_new += 1
                await db.commit()

            except DailyLimitReached:
                rate_limited = True
                break
            except Exception:
                logger.error("Scrape error for '%s':", kw, exc_info=True)

    # Deep screening (fetch full profiles)
    deep_screened = 0
    if session_valid:
        _step(request, "evaluating", "n8n: deep screening des profils...")
        async with get_db() as db:
            repo = ProspectRepository(db)
            cur = await db.execute(
                "SELECT id, linkedin_username FROM prospects "
                "WHERE (experience_json IS NULL OR experience_json = '' OR experience_json = '[]') "
                "AND linkedin_username IS NOT NULL "
                "ORDER BY created_at DESC LIMIT 20"
            )
            shallow = await cur.fetchall()

            for i, sp in enumerate(shallow):
                if i > 0:
                    pause = random.uniform(3, 8)
                    if random.random() < 0.2:
                        pause = random.uniform(15, 30)
                    await asyncio.sleep(pause)

                pid, username = sp[0], sp[1]
                _step(request, "evaluating", f"n8n: deep screen {i+1}/{len(shallow)}: {username}...")
                try:
                    profile = await scraper.get_person_profile(username)
                    if profile:
                        update = {}
                        for k, col in [("full_name", "full_name"), ("headline", "headline"),
                                        ("location", "location"), ("about", "about_text"),
                                        ("current_company", "current_company"), ("current_title", "current_title"),
                                        ("profile_photo_url", "profile_photo_url")]:
                            if profile.get(k):
                                update[col] = profile[k]
                        for k, col in [("experience", "experience_json"), ("education", "education_json"),
                                        ("skills", "skills_json")]:
                            if profile.get(k):
                                update[col] = json.dumps(profile[k])
                        update["screened_at"] = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
                        if update:
                            await repo.update(pid, update)
                            deep_screened += 1
                except DailyLimitReached:
                    break
                except Exception:
                    logger.error("Deep screen error for %s:", username, exc_info=True)
            await db.commit()

    return {
        "total_found": total_found,
        "total_new": total_new,
        "deep_screened": deep_screened,
        "rate_limited": rate_limited,
        "session_valid": session_valid,
    }



@router.post("/strategy/propose")
async def store_proposal(
    request: Request,
    proposed_program: str = Body(""),
    analysis: str = Body(""),
    run_id: int = Body(0),
):
    """Store strategy proposal and auto-accept if configured."""
    from app.config import settings

    _step(request, "proposing", "n8n: proposition de strategie recue")

    async with get_db() as db:
        if run_id:
            await db.execute("""
                UPDATE research_runs SET proposed_program = ?, proposal_reasoning = ? WHERE id = ?
            """, (proposed_program, (analysis or "")[:2000], run_id))
            await db.commit()

        applied_version = None
        if settings.AUTO_ACCEPT_IMPROVEMENTS and proposed_program:
            new_v = await research.apply_program(db, proposed_program, author="claude-auto-n8n")
            applied_version = new_v
            if run_id:
                await db.execute(
                    "UPDATE research_runs SET proposal_status = 'auto-accepted' WHERE id = ?",
                    (run_id,),
                )
                await db.execute(
                    "UPDATE prospect_program SET trigger = 'n8n-auto', change_reason = ? WHERE version = ?",
                    (f"Auto-accepted from n8n run#{run_id}", new_v),
                )
            await db.commit()
            logger.info("n8n KARPATHY KEEP: program v%d accepted", new_v)

    return {"ok": True, "applied_version": applied_version}


# --- Cycle finish ---

@router.post("/cycle/finish")
async def cycle_finish(
    request: Request,
    total_new: int = Body(0),
    total_found: int = Body(0),
    scored: int = Body(0),
    analyzed: int = Body(0),
    web_researched: int = Body(0),
    metrics: dict = Body(None),
):
    """Finalize cycle, record run, update LOOP_STATE."""
    ls = _loop(request)

    async with get_db() as db:
        v_cur = await db.execute(
            "SELECT version FROM prospect_program WHERE status = 'active' ORDER BY version DESC LIMIT 1"
        )
        v = await v_cur.fetchone()

        await db.execute("""
            INSERT INTO research_runs
                (program_version, finished_at, status, prospects_found, prospects_qualified, metric_json)
            VALUES (?, datetime('now'), ?, ?, ?, ?)
        """, (
            v[0] if v else 0,
            "completed" if total_new > 0 else "no_results",
            total_new,
            scored,
            json.dumps(metrics) if metrics else None,
        ))
        await db.commit()

        cur = await db.execute("SELECT last_insert_rowid()")
        run_id = (await cur.fetchone())[0]

    ls["status"] = "sleeping"
    ls["last_run_at"] = datetime.now().strftime("%Y-%m-%d %H:%M")
    ls["cycles_completed"] = ls.get("cycles_completed", 0) + 1
    ls["total_prospects_found"] = ls.get("total_prospects_found", 0) + total_new
    ls["total_qualified"] = ls.get("total_qualified", 0) + scored
    ls["last_error"] = None

    if total_new == 0:
        ls["consecutive_empty_runs"] = ls.get("consecutive_empty_runs", 0) + 1
        ls["current_step"] = f"n8n: cycle termine. 0 nouveaux (#{ls['consecutive_empty_runs']} consecutif)"
    else:
        ls["consecutive_empty_runs"] = 0
        ls["current_step"] = (
            f"n8n: cycle termine. +{total_new} prospects, {scored} scores, "
            f"{analyzed} analyses, {web_researched} recherches web"
        )

    ls["last_run_result"] = {
        "source": "n8n",
        "found": total_found,
        "new": total_new,
        "scored": scored,
        "analyzed": analyzed,
        "web_researched": web_researched,
    }

    return {"ok": True, "run_id": run_id}



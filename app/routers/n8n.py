@router.post("/analyze-batch")
async def analyze_batch(request: Request, limit: int = Body(5)):
    """Analyze unanalyzed prospects using Claude (Sonnet). CRM does the calls."""
    _step(request, "analyzing", "n8n: analyse Claude en cours...")

    deep_svc = DeepAnalysisService()
    analyzed = 0

    async with get_db() as db:
        _, prog_content = await research._load_program(db)
        acqs = await research._load_acquaintances(db)

        cur = await db.execute(
            "SELECT id FROM prospects "
            "WHERE experience_json IS NOT NULL AND experience_json != '' AND experience_json != '[]' "
            "AND (claude_analysis IS NULL OR claude_analysis = '') "
            "ORDER BY relevance_score DESC LIMIT ?",
            (limit,),
        )
        ids = [r[0] for r in await cur.fetchall()]

        from app.repositories.prospect_repo import ProspectRepository
        repo = ProspectRepository(db)

        for i, pid in enumerate(ids):
            _step(request, "analyzing", f"n8n: analyse Claude {i+1}/{len(ids)}...")
            prospect = await repo.find_by_id(pid)
            if not prospect:
                continue
            try:
                analysis = await deep_svc.analyze_prospect(prospect, prog_content, acqs)
                await repo.update(pid, {"claude_analysis": json.dumps(analysis, ensure_ascii=False)})
                analyzed += 1
                logger.info("n8n ANALYSIS [%d] %s -> verdict=%s",
                            pid, prospect.get("full_name", "?"), analysis.get("verdict"))
            except Exception:
                logger.error("n8n analysis error for %d:", pid, exc_info=True)
        await db.commit()

    return {"analyzed": analyzed, "total_pending": len(ids)}



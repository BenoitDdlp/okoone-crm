@router.post("/score")
async def score_prospects(request: Request):
    """Score all unscored prospects (algorithmic, no Claude)."""
    _step(request, "evaluating", "n8n: scoring algorithmique...")

    async with get_db() as db:
        cur = await db.execute("SELECT id FROM prospects WHERE relevance_score = 0 OR relevance_score IS NULL")
        unscored = [r[0] for r in await cur.fetchall()]

        w_cur = await db.execute("SELECT criteria_json FROM scoring_weights WHERE is_active = 1 LIMIT 1")
        w = await w_cur.fetchone()

        scored = 0
        if w and unscored:
            from app.services.scoring_service import ScoringService
            from app.repositories.prospect_repo import ProspectRepository

            scoring = ScoringService()
            repo = ProspectRepository(db)
            weights = json.loads(w[0])

            for pid in unscored:
                prospect = await repo.find_by_id(pid)
                if prospect:
                    score, breakdown = await scoring.score_prospect(prospect, weights)
                    summary = scoring.generate_score_summary(breakdown, weights, prospect)
                    await repo.update(pid, {
                        "relevance_score": score,
                        "score_breakdown": json.dumps(breakdown),
                        "score_summary": summary,
                        "status": "screened",
                    })
                    scored += 1
            await db.commit()

    return {"scored": scored, "total_unscored": len(unscored)}



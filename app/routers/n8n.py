def _loop(request: Request) -> dict:
    return getattr(request.app.state, "loop_state", {})


def _step(request: Request, status: str, step: str):
    ls = _loop(request)
    ls["status"] = status
    ls["current_step"] = step
    logger.info("n8n step: [%s] %s", status, step)


# --- Cycle lifecycle ---

@router.post("/cycle/start")
async def cycle_start(request: Request):
    """Mark cycle as running. Returns cycle context (program, weights, etc)."""
    ls = _loop(request)
    ls["active"] = True
    _step(request, "searching", "n8n: cycle demarre...")

    async with get_db() as db:
        cur = await db.execute(
            "SELECT version, content FROM prospect_program WHERE status = 'active' ORDER BY version DESC LIMIT 1"
        )
        prog = await cur.fetchone()

        cur2 = await db.execute("SELECT * FROM acquaintances ORDER BY created_at DESC")
        acqs = [dict(r) for r in await cur2.fetchall()]

        cur3 = await db.execute("SELECT criteria_json FROM scoring_weights WHERE is_active = 1 LIMIT 1")
        w = await cur3.fetchone()

        cur4 = await db.execute("""
            SELECT COUNT(*) as total,
                   SUM(CASE WHEN hr.reviewer_verdict = 'approve' THEN 1 ELSE 0 END) as approved,
                   SUM(CASE WHEN hr.reviewer_verdict = 'reject' THEN 1 ELSE 0 END) as rejected
            FROM prospects p LEFT JOIN human_reviews hr ON hr.prospect_id = p.id
            WHERE p.created_at > datetime('now', '-7 days')
        """)
        stats = dict(await cur4.fetchone())

        cur5 = await db.execute(
            "SELECT DISTINCT keywords FROM search_queries ORDER BY created_at DESC LIMIT 100"
        )
        past = [r[0] for r in await cur5.fetchall()]

        metrics_history = await research._load_metrics_history(db, limit=5)
        query_perf = await research._load_query_performance(db, limit=15)

    return {
        "program_version": prog[0] if prog else 0,
        "program_content": prog[1] if prog else "",
        "acquaintances": acqs,
        "weights": json.loads(w[0]) if w else {},
        "stats_7d": stats,
        "past_queries": past,
        "metrics_history": metrics_history,
        "query_performance": query_perf,
    }


@router.post("/cycle/step")
async def cycle_step(request: Request, status: str = Body(""), step: str = Body("")):
    """Update LOOP_STATE from n8n (progress reporting)."""
